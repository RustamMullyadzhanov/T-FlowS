!======================================================================!
  SUBROUTINE IniDiv 
!----------------------------------------------------------------------!
!   Initialize processor numbers to 1                                  !
!----------------------------------------------------------------------!
!------------------------------[Modules]-------------------------------!
  USE all_mod
  USE par_mod 
  USE div_mod 
!----------------------------------------------------------------------!
  IMPLICIT NONE
!-------------------------------[Locals]-------------------------------!
  INTEGER :: c
!======================================================================!

!------------------------------!
!     Initialize parameters    !
!------------------------------!
  COORDINATE = 1
  INERTIAL   = 2

!------------------------------!
!     Initialize processors    !
!------------------------------!
  do c=1,NC
    proces(c)=1
  end do

  END SUBROUTINE IniDiv
