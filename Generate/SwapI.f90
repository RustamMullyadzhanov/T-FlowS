!======================================================================!
  SUBROUTINE SwapI(a,b) 
!----------------------------------------------------------------------!
!   Swaps two integers.                                                !
!----------------------------------------------------------------------!
  IMPLICIT NONE
!-----------------------------[Parameters]-----------------------------!
  INTEGER :: a,b  
!-------------------------------[Locals]-------------------------------!
  INTEGER :: t
!======================================================================!

  t=a
  a=b
  b=t

  END SUBROUTINE SwapI
