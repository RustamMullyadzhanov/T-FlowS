!======================================================================!
  REAL FUNCTION Dist2(xA,yA,zA,xB,yB,zB)
!----------------------------------------------------------------------!
!  Calculates distance between two points.                             !
!----------------------------------------------------------------------!
  IMPLICIT NONE
!-----------------------------[Parameters]-----------------------------!
  REAL :: xA,yA,zA,xB,yB,zB
!======================================================================!

  Dist2=(xA-xB)*(xA-xB) + (yA-yB)*(yA-yB) + (zA-zB)*(zA-zB)

  END FUNCTION Dist2
