!======================================================================!
  real function DistR(rA,zA,rB,zB)
!----------------------------------------------------------------------!
!  Calculates distance between two points.                             !
!----------------------------------------------------------------------!
  implicit none
!-----------------------------[Parameters]-----------------------------!
  real :: rA,zA,rB,zB
!======================================================================!

  DistR=sqrt( (rA-rB)*(rA-rB) + (zA-zB)*(zA-zB) )

  end function DistR
