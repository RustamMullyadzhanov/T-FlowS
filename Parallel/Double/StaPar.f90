!======================================================================!
  SUBROUTINE StaPar
!----------------------------------------------------------------------!
!   Initializes parallel execution.                                    !
!----------------------------------------------------------------------!
!------------------------------[Modules]-------------------------------!
  USE par_mod 
!----------------------------------------------------------------------!
  IMPLICIT NONE
!------------------------------[Include]-------------------------------!
  INCLUDE 'mpif.h'
!-------------------------------[Locals]-------------------------------!
  INTEGER :: error
!======================================================================!

  CALL MPI_INIT(ERROR)

  CALL MPI_COMM_SIZE(MPI_COMM_WORLD,                                &
                     NPro,                                          &
                     error) 

  CALL MPI_COMM_RANK(MPI_COMM_WORLD,                                &
                     this,                                          &
                     error)

  this=this+1

  if(Npro == 1) then
    Npro = 0
    this = 0
  endif

  END SUBROUTINE StaPar
