!======================================================================!
  SUBROUTINE Wait 
!----------------------------------------------------------------------!
!   Puts barrier for parallel execution.                               !
!----------------------------------------------------------------------!
  IMPLICIT NONE
!------------------------------[Include]-------------------------------!
  INCLUDE 'mpif.h'
!-------------------------------[Locals]-------------------------------!
  INTEGER :: error
!======================================================================!

!==================================
      call MPI_BARRIER        &
!----------------------------------
             (MPI_COMM_WORLD, &
              error) 
!==================================

  END SUBROUTINE Wait
